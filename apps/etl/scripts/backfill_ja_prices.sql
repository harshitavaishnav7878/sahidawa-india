-- 1. Exact generic name and strength match (Priority 1)
UPDATE public.medicines m
SET jan_aushadhi_price = n.mrp
FROM nppa_lookup n
WHERE m.source = 'commercial'
  AND m.jan_aushadhi_price IS NULL
  AND n.generic_name_lower = REGEXP_REPLACE(
      REPLACE(
          REPLACE(
              REPLACE(
                  LOWER(TRIM(REGEXP_REPLACE(m.generic_name, '\s*\([^)]*\)', '', 'g'))),
                  'amoxycillin', 'amoxicillin'
              ),
              'clavulanic acid', 'clavulanate'
          ),
          'clavulanic', 'clavulanate'
      ),
      '\s+', ' ', 'g'
  )
  AND n.strength_lower = LOWER(REPLACE(TRIM(m.strength), ' ', ''));

-- 2. Generic name fallback match (Priority 2 - where strength is NULL in lookup)
UPDATE public.medicines m
SET jan_aushadhi_price = n.mrp
FROM nppa_lookup n
WHERE m.source = 'commercial'
  AND m.jan_aushadhi_price IS NULL
  AND n.generic_name_lower = REGEXP_REPLACE(
      REPLACE(
          REPLACE(
              REPLACE(
                  LOWER(TRIM(REGEXP_REPLACE(m.generic_name, '\s*\([^)]*\)', '', 'g'))),
                  'amoxycillin', 'amoxicillin'
              ),
              'clavulanic acid', 'clavulanate'
          ),
          'clavulanic', 'clavulanate'
      ),
      '\s+', ' ', 'g'
  )
  AND n.strength_lower IS NULL;

DROP TABLE nppa_lookup;
